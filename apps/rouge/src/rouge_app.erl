start(_StartType, _StartArgs) ->
    lager:info("hello",[]),
    Dispatch = cowboy_router:compile([
                                      {'_', [
                                             {"/", rouge_socket_handler, []}
                                            ]}
                                     ]),
    {ok, _} = cowboy:start_http(http, 100, [{port, 8080}],
                                [{env, [{dispatch, Dispatch}]}]),
    'rouge_sup':start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.


